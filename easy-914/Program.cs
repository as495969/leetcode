        public class Solution
        {
            public static bool HasGroupsSizeX(int[] deck)
            {
                if (deck.Length < 2) return false;
                bool isTrue = true;
                Array.Sort(deck);
                Dictionary<int, int> dic = new Dictionary<int, int>();

                #region 统计各数字出现的次数

                for (int i = 0; i < deck.Length; i++)
                {
                    if (dic.ContainsKey(deck[i]))
                    {
                        dic[deck[i]]++;
                    }
                    else
                    {
                        dic.Add(deck[i], 1);
                    }
                }
                #endregion

                #region 获取最少次数

                int minNum = int.MaxValue;
                foreach (KeyValuePair<int, int> keyValuePair in dic)
                {
                    if (keyValuePair.Value < minNum) minNum = keyValuePair.Value;
                }

                #endregion

                //每个数对2到最小次数求余,!=0就break;,==0就count++,最后count==dic.count,就说明i是所有次数的公约数
                if (minNum > 2)
                {
                    for (int i = 2; i < minNum +1; i++)
                    {
                        int count = 0;
                        foreach (KeyValuePair<int, int> keyValuePair in dic)
                        {
                            if (keyValuePair.Value % i != 0)
                            {
                                break;
                            }
                            else
                            {
                                count++;
                            }
                        }

                        if (count == dic.Count)
                        {
                            return true;
                        }
                    }
                }
                else
                {
                    int count = 0;
                    foreach (KeyValuePair<int, int> keyValuePair in dic)
                    {
                        if (keyValuePair.Value % 2 != 0)
                        {
                            break;
                        }
                        else
                        {
                            count++;
                        }
                    }

                    if (count == dic.Count)
                    {
                        return true;
                    }
                    else
                    {
                        return false;
                    }
                }
                return isTrue;
            }
        }
